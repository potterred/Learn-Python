# Passo 1: Pegar o primeiro valor
primeiro_valor = float(input("Digite um valor: "))

# Passo 2: Pegar o segundo valor
segundo_valor = float(input("Digite outro valor: "))

# Passo 3: Comparar os dois valores
if primeiro_valor >= segundo_valor:
    print("O primeiro valor é maior ou igual ao segundo.")
else:
    print("O segundo valor é maior.")
